package com.example.recicleview;

public class Modelo {
    private String nombreDirectorPelicula;
    private String nombrePelicula;
    private String generoPelicula;
    private String anioPelicula;
    private int imagenPelicula;
    private String duracionPelicula;
    private String fechaPelicula;
    private double clasificacionPelicula;

    public String getNombreDirectorPelicula() {
        return nombreDirectorPelicula;
    }

    public void setNombreDirectorPelicula(String nombreDirectorPelicula) {
        this.nombreDirectorPelicula = nombreDirectorPelicula;
    }

    public String getNombrePelicula() {
        return nombrePelicula;
    }

    public void setNombrePelicula(String nombrePelicula) {
        this.nombrePelicula = nombrePelicula;
    }

    public String getGeneroPelicula() {
        return generoPelicula;
    }

    public void setGeneroPelicula(String generoPelicula) {
        this.generoPelicula = generoPelicula;
    }

    public String getAnioPelicula() {
        return anioPelicula;
    }

    public void setAnioPelicula(String anioPelicula) {
        this.anioPelicula = anioPelicula;
    }

    public int getImagenPelicula() {
        return imagenPelicula;
    }

    public void setImagenPelicula(int imagenPelicula) {
        this.imagenPelicula = imagenPelicula;
    }

    public String getDuracionPelicula() {
        return duracionPelicula;
    }

    public void setDuracionPelicula(String duracionPelicula) {
        this.duracionPelicula = duracionPelicula;
    }

    public String getFechaPelicula() {
        return fechaPelicula;
    }

    public void setFechaPelicula(String fechaPelicula) {
        this.fechaPelicula = fechaPelicula;
    }

    public double getClasificacionPelicula() {
        return clasificacionPelicula;
    }

    public void setClasificacionPelicula(double clasificacionPelicula) {
        this.clasificacionPelicula = clasificacionPelicula;
    }
}
